import turtle

# Set the color of the pen to blue and begin to fill in the shape's color.
turtle.color("blue")
turtle.begin_fill()

# Draw the octagon shape
turtle.forward(100)
turtle.right(45)
turtle.forward(100)
turtle.right(45)
turtle.forward(100)
turtle.right(45)
turtle.forward(100)
turtle.right(45)
turtle.forward(100)
turtle.right(45)
turtle.forward(100)
turtle.right(45)
turtle.forward(100)
turtle.right(45)
turtle.forward(100)

# Fill in the shape blue.
turtle.end_fill()
